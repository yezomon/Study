SELECT 
    A.ID,
    COUNT(B.ID) AS CHILD_COUNT
FROM ECOLI_DATA A
LEFT OUTER JOIN ECOLI_DATA B
ON A.ID = B.PARENT_ID
GROUP BY A.ID
;
-- ORDER BY ID
-- Null 있는 컬럼은 GROUP BY 하면 오류남..

