SELECT *
FROM (SELECT UG.USER_ID, UG.NICKNAME, SUM(UB.PRICE) AS TOTAL_SALES
FROM USED_GOODS_BOARD UB
LEFT OUTER JOIN USED_GOODS_USER UG
ON UB.WRITER_ID = UG.USER_ID
WHERE UB.STATUS = 'DONE'
GROUP BY UB.WRITER_ID
ORDER BY TOTAL_SALES ASC) AS T
WHERE T.TOTAL_SALES >= 700000
;
